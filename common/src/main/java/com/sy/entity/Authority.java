package com.sy.entity;

import com.fasterxml.jackson.annotation.JsonFormat;

import javax.persistence.*;
import java.sql.Timestamp;
import java.util.List;
import java.util.Objects;

@Entity
public class Authority {
    private int id;
    private String authorityName;
    private Integer level;
    private Integer pid;
    private String status;
    @JsonFormat(pattern = "yyyy-MM-dd HH:mm:ss", timezone = "GMT+8")
    private Timestamp createTime;
    @JsonFormat(pattern = "yyyy-MM-dd HH:mm:ss", timezone = "GMT+8")
    private Timestamp updateTime;
    private String remark;
    
    @Transient
    private List<Authority> sList;
    
    public Authority() {
    }

    public Authority(int id) {
        this.id = id;
    }

    @Id
    @Column(name = "id")
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Basic
    @Column(name = "authority_name")
    public String getAuthorityName() {
        return authorityName;
    }

    public void setAuthorityName(String authorityName) {
        this.authorityName = authorityName;
    }

    @Basic
    @Column(name = "level")
    public Integer getLevel() {
        return level;
    }

    public void setLevel(Integer level) {
        this.level = level;
    }

    @Basic
    @Column(name = "pid")
    public Integer getPid() {
        return pid;
    }

    public void setPid(Integer pid) {
        this.pid = pid;
    }

    @Basic
    @Column(name = "status")
    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    @Basic
    @Column(name = "create_time")
    public Timestamp getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Timestamp createTime) {
        this.createTime = createTime;
    }

    @Basic
    @Column(name = "update_time")
    public Timestamp getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Timestamp updateTime) {
        this.updateTime = updateTime;
    }

    @Basic
    @Column(name = "remark")
    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
    
    @Transient
    public List<Authority> getsList() {
		return sList;
	}

	public void setsList(List<Authority> sList) {
		this.sList = sList;
	}

	@Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Authority authority = (Authority) o;
        return id == authority.id &&
                Objects.equals(authorityName, authority.authorityName) &&
                Objects.equals(level, authority.level) &&
                Objects.equals(pid, authority.pid) &&
                Objects.equals(status, authority.status) &&
                Objects.equals(createTime, authority.createTime) &&
                Objects.equals(updateTime, authority.updateTime) &&
                Objects.equals(remark, authority.remark);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, authorityName, level, pid, status, createTime, updateTime, remark);
    }

	@Override
	public String toString() {
		return "Authority [id=" + id + ", authorityName=" + authorityName + ", level=" + level + ", pid=" + pid
				+ ", status=" + status + ", createTime=" + createTime + ", updateTime=" + updateTime + ", remark="
				+ remark + ", sList=" + sList + "]";
	}
    
}
